import { NextRequest, NextResponse } from 'next/server';
import { googleCloudStorageService } from '@/lib/google-cloud-storage-service-fixed';
import { resolveUserId } from '@/lib/auth-user';

interface QuestObjective {
  id: string;
  description: string;
  completed: boolean;
  dateCompleted?: string;
  gameDay?: number;
  gameHour?: number;
}

interface JournalEntry {
  id: string;
  date: string;
  title: string;
  content: string;
  category: string;
  tags: string[];
  isAutoGenerated: boolean;
  sessionId?: string;
  type?: string;
  gameDay?: number;
  gameHour?: number;
  questStatus?: string;
  objectives?: QuestObjective[];
}

interface CloudJournal {
  id: string;
  name: string;
  date: string;
  entries: JournalEntry[];
  userId: string;
  lastUpdated: string;
  version: string;
  size: number;
}

// Generowanie unikalnego ID użytkownika (na podstawie IP i user agent)
function generateUserId(request: NextRequest): string {
  const ip = request.headers.get('x-forwarded-for') || 'unknown';
  const userAgent = request.headers.get('user-agent') || 'unknown';
  const combined = `${ip}-${userAgent}`;

  // Prosta funkcja hash
  let hash = 0;
  for (let i = 0; i < combined.length; i++) {
    const char = combined.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash = hash & hash; // Convert to 32bit integer
  }
  return `user_${Math.abs(hash)}`;
}

// POST - Zapisywanie dziennika do chmury
export async function POST(request: NextRequest) {
  try {
    const { journalData, journalId, userId } = await request.json();

    if (!journalData) {
      return NextResponse.json(
        { error: 'Journal data is required' },
        { status: 400 }
      );
    }

    const finalUserId = await resolveUserId(userId || generateUserId(request));
    const finalJournalId = journalId || `journal_${Date.now()}`;

    // Przygotowanie danych dziennika
    const cloudJournal: CloudJournal = {
      ...journalData,
      id: finalJournalId,
      userId: finalUserId,
      lastUpdated: new Date().toISOString(),
      version: '1.0',
      size: JSON.stringify(journalData).length,
    };

    // Nazwa pliku w Google Cloud Storage
    const fileName = `journals/${finalUserId}/${finalJournalId}.json`;

    // Upload do Google Cloud Storage
    const url = await googleCloudStorageService.uploadJSON(
      cloudJournal,
      fileName
    );

    return NextResponse.json({
      success: true,
      message: 'Journal saved to cloud successfully',
      journalId: finalJournalId,
      userId: finalUserId,
      url: url,
      size: cloudJournal.size,
      lastUpdated: cloudJournal.lastUpdated,
    });
  } catch (error) {
    console.error('Error saving journal to cloud:', error);

    if (error instanceof Error) {
      if (
        error.message.includes('GOOGLE_CLOUD') ||
        error.message.includes('key') ||
        error.message.includes('credentials')
      ) {
        return NextResponse.json(
          {
            error:
              'Błąd konfiguracji Google Cloud Storage. Skontaktuj się z pomocą techniczną',
            details: 'See IMPLEMENTATION_GUIDE.md for instructions',
          },
          { status: 500 }
        );
      }
    }

    return NextResponse.json(
      {
        error: 'Failed to save journal to cloud',
        details: error instanceof Error ? error.message : 'Unknown error',
      },
      { status: 500 }
    );
  }
}

// GET - Ładowanie dziennika z chmury
export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const userId = searchParams.get('userId');
    const journalId = searchParams.get('journalId');
    const listAll = searchParams.get('listAll') === 'true';
    const test = searchParams.get('test') === 'true';

    // Test endpoint - sprawdź czy API jest dostępne
    if (test) {
      return NextResponse.json({
        status: 'available',
        message: 'Journal API is configured and ready',
      });
    }

    const finalUserId = await resolveUserId(userId || generateUserId(request));

    if (listAll) {
      // Lista wszystkich dzienników użytkownika
      return await listUserJournals(finalUserId);
    } else if (journalId) {
      // Pojedynczy dziennik
      return await getSingleJournal(finalUserId, journalId);
    } else {
      // Domyślnie zwróć najnowszy dziennik użytkownika
      return await getLatestJournal(finalUserId);
    }
  } catch (error) {
    console.error('Error loading journal from cloud:', error);
    return NextResponse.json(
      {
        error: 'Failed to load journal from cloud',
        details: error instanceof Error ? error.message : 'Unknown error',
      },
      { status: 500 }
    );
  }
}

