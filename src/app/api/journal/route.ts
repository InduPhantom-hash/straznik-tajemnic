// DELETE - Usuwanie dziennika z chmury
export async function DELETE(request: NextRequest) {
  try {
    const body = await request.json().catch(() => ({}));
    const { journalId, userId } = body;

    if (!journalId) {
      // Jeśli brak ID, uznajemy to za prośbę o wyczyszczenie (lub sukces bez akcji)
      // W przypadku GCS nie chcemy usuwać wszystkiego bez potwierdzenia, ale
      // zwrócenie sukcesu uciszy błędy na froncie.
      return NextResponse.json({
        success: true,
        message: 'Journal clear requested (no specific ID provided)',
      });
    }

    const finalUserId = await resolveUserId(userId || generateUserId(request));
    const fileName = `journals/${finalUserId}/${journalId}.json`;

    // Usunięcie z Google Cloud Storage
    await googleCloudStorageService.deleteFile(fileName);

    return NextResponse.json({
      success: true,
      message: 'Journal deleted from cloud successfully',
      journalId,
    });
  } catch (error) {
    console.error('Error deleting journal from cloud:', error);
    return NextResponse.json(
      {
        error: 'Failed to delete journal from cloud',
        details: error instanceof Error ? error.message : 'Unknown error',
      },
      { status: 500 }
    );
  }
}

// Funkcje pomocnicze
async function listUserJournals(userId: string) {
  try {
    const files = await googleCloudStorageService.listFiles(
      `journals/${userId}/`
    );

    const journals = await Promise.all(
      files.map(async (file) => {
        try {
          const buffer = await googleCloudStorageService.downloadFile(
            file.name
          );
          const journalData = JSON.parse(buffer.toString());
          return {
            id: journalData.id,
            name: journalData.name,
            date: journalData.date,
            lastUpdated: journalData.lastUpdated,
            size: journalData.size,
            entriesCount: journalData.entries?.length || 0,
            url: file.publicUrl,
          };
        } catch (error) {
          console.error(`Error loading journal ${file.name}:`, error);
          return null;
        }
      })
    );

    const validJournals = journals.filter((journal) => journal !== null);

    return NextResponse.json({
      success: true,
      journals: validJournals,
      totalCount: validJournals.length,
      userId,
    });
  } catch (error) {
    console.error('Error listing user journals:', error);
    return NextResponse.json(
      { error: 'Failed to list journals' },
      { status: 500 }
    );
  }
}

async function getSingleJournal(userId: string, journalId: string) {
  try {
    const fileName = `journals/${userId}/${journalId}.json`;

    try {
      const buffer = await googleCloudStorageService.downloadFile(fileName);
      const journalData = JSON.parse(buffer.toString());

      return NextResponse.json({
        success: true,
        journal: journalData,
        url: `https://storage.googleapis.com/${process.env.GOOGLE_CLOUD_STORAGE_BUCKET}/${fileName}`,
      });
    } catch {
      return NextResponse.json({ error: 'Journal not found' }, { status: 404 });
    }
  } catch (error) {
    console.error('Error getting single journal:', error);
    return NextResponse.json(
      { error: 'Failed to get journal' },
      { status: 500 }
    );
  }
}

async function getLatestJournal(userId: string) {
  try {
    const files = await googleCloudStorageService.listFiles(
      `journals/${userId}/`
    );

    if (files.length === 0) {
      // Jeśli nie ma dzienników, zwróć pusty dziennik
      return NextResponse.json({
        success: true,
        journal: {
          id: `journal_${Date.now()}`,
          name: 'Mój Dziennik',
          date: new Date().toISOString(),
          entries: [],
          userId,
          lastUpdated: new Date().toISOString(),
          version: '1.0',
          size: 0,
        },
        isNew: true,
      });
    }

    // Znajdź najnowszy dziennik
    const latestFile = files.reduce((latest, current) => {
      return new Date(current.updated) > new Date(latest.updated)
        ? current
        : latest;
    });

    const buffer = await googleCloudStorageService.downloadFile(
      latestFile.name
    );
    const journalData = JSON.parse(buffer.toString());

    return NextResponse.json({
      success: true,
      journal: journalData,
      url: latestFile.publicUrl,
    });
  } catch (error) {
    console.error('Error getting latest journal:', error);
    return NextResponse.json(
      { error: 'Failed to get latest journal' },
      { status: 500 }
    );
  }
}
